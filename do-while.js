
// do while loop
console.log('count one to five using do while loop');
const count = [1, 2, 3, 4, 5];

let j = 0;
do {
  console.log(count[j]);
  j++;
} while (j < count.length);



console.log("Countdown from 5 to 1 using a do...while loop:");
let counts = 5;
do {
  console.log(counts);
  counts--;
} while (counts >= 1);

