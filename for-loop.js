// for loop
console.log("print one to ten");

for(i=1;i<=10;i++){
console.log(i);
}

// print even number
console.log("Even numbers using a for loop:");

for (let i = 2; i <= 10; i += 2) {
  console.log(i);
}

console.log("Reverse from 10 to 1 using a for loop:");
for (let i = 10; i >= 1; i--) {
  console.log(i);
}